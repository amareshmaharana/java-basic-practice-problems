public class FeverTemp {
    public static void main(String[] args) {
        double temp = 103.5;

        if(temp > 100) {
            System.out.println("Oh no!!, it's " + temp + " centigrade" + " you have a fever. hurry up!!!");
        } else {
            System.out.println("Don't worry, you are safe. Have a good health!!!");
        }
    }
}
